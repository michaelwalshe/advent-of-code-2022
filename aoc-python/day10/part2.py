def compute(s: str) -> int:
    dx = []
    x = [1]
    for line in s.splitlines():
        if line != "noop":
            op, v = line.split()
            dx.extend([0, int(v)])
        else:
            dx.append(0)
    for d in dx:
        x.append(x[-1] + d)

    crt = ""
    for j in range(6):
        for i in range(40):
            cycle = i + (40 * j)
            posn = x[cycle]
            if i in (posn - 1, posn, posn + 1):
                crt += "#"
            else:
                crt += "."
        crt += "\n"
    return crt


